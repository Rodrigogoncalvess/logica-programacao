public class ReutilizandoVariaveis {
    public static void main(String[] args) {

        double salario = 17.00;
        double resultado = salario * 0.5;

        System.out.println("Calculando a porcentagem: " + resultado);
        resultado = salario * 0.19;
        System.out.println("Calculando a porcentagem: " + resultado);
    }


}
